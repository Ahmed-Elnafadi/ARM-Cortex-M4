 /* APB BUS */
 
 #define GPIO_BASE_ADDRESS(x)					(x<4? (0x40004000+(0x1000*(x))): (0x40024000+(1000*(x-4))))
 
 #define GPIO_DATA(x)							*((volatile uint32_t*) GPIO_BASE_ADDRESS(x)+ 0x000)
 #define GPIO_DIR(x)							*((volatile uint32_t*) GPIO_BASE_ADDRESS(x)+ 0x400)
 #define GPIO_AFSEL(x)							*((volatile uint32_t*) GPIO_BASE_ADDRESS(x)+ 0x420)
 #define GPIO_DR2R(x)							*((volatile uint32_t*) GPIO_BASE_ADDRESS(x)+ 0x500)
 #define GPIO_DR4R(x)							*((volatile uint32_t*) GPIO_BASE_ADDRESS(x)+ 0x504)
 #define GPIO_DR8R(x)							*((volatile uint32_t*) GPIO_BASE_ADDRESS(x)+ 0x508)
 #define GPIO_ODR(x)							*((volatile uint32_t*) GPIO_BASE_ADDRESS(x)+ 0x50C)
 #define GPIO_PUR(x)							*((volatile uint32_t*) GPIO_BASE_ADDRESS(x)+ 0x510)
 #define GPIO_PDR(x)							*((volatile uint32_t*) GPIO_BASE_ADDRESS(x)+ 0x514)
 #define GPIO_DEN(x)							*((volatile uint32_t*) GPIO_BASE_ADDRESS(x)+ 0x51C)
 #define GPIO_AMSEL(x)							*((volatile uint32_t*) GPIO_BASE_ADDRESS(x)+ 0x528)
 
