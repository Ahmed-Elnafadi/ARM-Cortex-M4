 #include "STD_TYPES.h"
 
/*******************************************************************************************************************************
 * GLOBAL DATA TYPES AND STRUCTURES
 ******************************************************************************************************************************/
/* Interrupt Enable Registers */
typedef struct{
	uint32_t	EN_0;
	uint32_t  EN_1;
	uint32_t	EN_2;
	uint32_t	EN_3;
	
}EN_Int_t;

/* Interrupt Disable Registers */
typedef struct{
	
	uint32_t  DIS_0;
	uint32_t	DIS_1;
	uint32_t	DIS_2;
	uint32_t	DIS_3;
	
}DIS_Int_t;

/* Interrupt Pending Registers */
typedef struct{
	uint32_t	PEND_0;
	uint32_t  PEND_1;
	uint32_t	PEND_2;
	uint32_t	PEND_3;
	
}PEN_Int_t;

/* Interrupt Unpending Registers */
typedef struct{
	uint32_t	UNPEND_0;
	uint32_t  UNPEND_1;
	uint32_t	UNPEND_2;
	uint32_t	UNPEND_3;
	
}UNPEND_Int_t;

/* Reading which Interrupt is active (Read Only)*/
typedef struct{
	uint32_t	ACTIVE_0;
	uint32_t  ACTIVE_1;
	uint32_t	ACTIVE_2;
	uint32_t	ACTIVE_3;
	
}ACTIVE_Int_t;
/*******************************************************************************************************************************
 * GLOBAL CONSTANT MACROS
 ******************************************************************************************************************************/
#define BASE_ADDRESS			    0xE000E000
#define NVIC_EN_REGISTER			((volatile	EN_Int_t*) BASE_ADDRESS+0x100)
#define NVIC_DIS_REGISTER			((volatile DIS_Int_t*) BASE_ADDRESS+0x180)
#define NVIC_PEND_REGISTER			((volatile PEN_Int_t*) BASE_ADDRESS+200)
#define NVIC_UNPEND_REGISTER		((volatile UNPEND_Int_t*)BASE_ADDRESS+0x280)
#define NVIC_ACTIVE_REGISTER		((volatile ACTIVE_Int_t*) BASE_ADDRESS+0x300)
#define SCB_APINT_REGISTER			*((volatile uint32_t*)BASE_ADDRESS+0xD0C)
